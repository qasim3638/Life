"""
SEO Autopilot — all-in-one autonomous SEO maintenance.

Five jobs, all auto-fixing where it's safe and alerting where it's not:

  1. cannibalization_autopilot     (daily 07:00) — when two URLs target the
     same query, auto-set rel=canonical on the loser → winner.
  2. stale_page_autopilot          (weekly Sun 07:30) — pages 180+ days old
     with declining impressions get auto-regenerated by the existing AI.
  3. redirect_404_autopilot        (daily 07:15) — 404 hits in the
     last 24h get auto-301 to the closest matching active product /
     category. Logged for audit, never irreversible.
  4. algorithm_update_detector     (daily 08:00) — sudden traffic shift
     vs 7-day rolling avg → email + Telegram alert.
  5. brand_serp_tracker            (weekly Mon 07:45) — snapshots GSC
     positions for brand+location queries, alerts on big drops.

Every action gets an audit row in `seo_autopilot_actions` with what,
why, when, and (where applicable) a one-click revert pointer.
"""
from __future__ import annotations

import logging
import re
from datetime import datetime, timedelta, timezone
from typing import Any

from config import get_db

logger = logging.getLogger(__name__)

ACTIONS = "seo_autopilot_actions"
REDIRECTS = "seo_redirects"
NOT_FOUND_LOG = "seo_404_log"


async def _log_action(action_type: str, **fields):
    db = get_db()
    doc = {"action_type": action_type, "ts": datetime.now(timezone.utc), **fields}
    await db[ACTIONS].insert_one(doc)


# ────────────────────────────────────────────────────────────────────────
# 1) Cannibalization autopilot
# ────────────────────────────────────────────────────────────────────────


async def run_cannibalization_autopilot() -> dict[str, Any]:
    """Find queries where ≥2 of our URLs are competing in the top 20.
    Auto-add a rel=canonical pointer from the loser to the winner.

    The canonical record is stored in `seo_canonical_overrides` and
    consumed by the SEO meta injector at render time. Conservative
    fix — adding canonical never deletes content, Google honours it
    within 1-2 weeks, and removing the override later is one delete.
    """
    db = get_db()
    # Re-use the GSC token-picker so we run as the connected admin.
    from services import gsc as gsc_service
    admin_doc = await db["gsc_oauth_tokens"].find_one(
        {}, sort=[("last_used_at", -1)], projection={"_id": 1},
    )
    if not admin_doc:
        return {"ok": True, "skipped": True, "reason": "no_gsc"}

    try:
        # We need query-by-page data — call the GSC service helper with
        # 2 dimensions. Returns rows already keyed by dimension name.
        from services.gsc import _query
        result = await _query(
            admin_doc["_id"], dimensions=["query", "page"], days=28, row_limit=2500,
        )
        rows = result.get("rows") or []
    except Exception as exc:  # noqa: BLE001
        logger.info("cannibalization: GSC pull failed: %s", exc)
        return {"ok": True, "skipped": True, "reason": f"gsc_error: {exc}"}

    by_query: dict[str, list[dict[str, Any]]] = {}
    for r in rows:
        q = r.get("query")
        page = r.get("page")
        if not q or not page:
            continue
        by_query.setdefault(q, []).append({
            "page": page, "clicks": int(r.get("clicks") or 0),
            "impressions": int(r.get("impressions") or 0),
            "position": float(r.get("position") or 0),
        })

    overrides_written = 0
    for q, urls in by_query.items():
        if len(urls) < 2:
            continue
        # Both URLs must be ranking respectably (top 20) and have non-trivial impressions
        contenders = [u for u in urls if u["position"] <= 20 and u["impressions"] >= 50]
        if len(contenders) < 2:
            continue
        contenders.sort(key=lambda x: (-x["clicks"], x["position"]))
        winner = contenders[0]
        for loser in contenders[1:]:
            if loser["page"] == winner["page"]:
                continue
            from urllib.parse import urlparse
            loser_path = urlparse(loser["page"]).path
            winner_url = winner["page"]
            res = await db.seo_canonical_overrides.update_one(
                {"_id": loser_path},
                {"$set": {
                    "_id": loser_path,
                    "canonical_url": winner_url,
                    "reason": "cannibalization_autopilot",
                    "query": q,
                    "loser_clicks": loser["clicks"],
                    "winner_clicks": winner["clicks"],
                    "applied_at": datetime.now(timezone.utc),
                }},
                upsert=True,
            )
            if res.upserted_id or res.modified_count:
                overrides_written += 1
                await _log_action(
                    "cannibalization_canonical",
                    query=q, loser_path=loser_path, winner_url=winner_url,
                    loser_clicks=loser["clicks"], winner_clicks=winner["clicks"],
                )
    return {"ok": True, "overrides_written": overrides_written}


# ────────────────────────────────────────────────────────────────────────
# 2) Stale page autopilot
# ────────────────────────────────────────────────────────────────────────


async def run_stale_page_autopilot() -> dict[str, Any]:
    """Auto-refresh content that's gone stale.

    Definition of stale: city landing pages with `approved_at` (or
    `updated_at`) older than 180 days. We re-trigger the existing
    AI city-page regenerator so the content is rewritten without
    a human in the loop.
    """
    db = get_db()
    cutoff = datetime.now(timezone.utc) - timedelta(days=180)

    cursor = db.city_landing_pages.find(
        {
            "status": "approved",
            "$or": [
                {"approved_at": {"$lt": cutoff}},
                {"approved_at": {"$exists": False}, "updated_at": {"$lt": cutoff}},
            ],
        },
        {"_id": 0, "slug": 1, "city": 1, "tile_type": 1, "approved_at": 1, "updated_at": 1},
    ).limit(5)
    rows = await cursor.to_list(length=5)

    refreshed = 0
    for row in rows:
        try:
            from services.city_pages_autogen import regenerate_city_page  # type: ignore
            res = await regenerate_city_page(slug=row["slug"], reason="stale_autopilot")
            if (res or {}).get("ok"):
                refreshed += 1
                await _log_action(
                    "stale_page_refresh",
                    slug=row["slug"], age_days=180,
                )
        except ImportError:
            # If the regen helper isn't exposed yet, just stamp the page
            # so the next nightly autogen picks it up.
            await db.city_landing_pages.update_one(
                {"slug": row["slug"]},
                {"$set": {"needs_refresh": True, "marked_stale_at": datetime.now(timezone.utc)}},
            )
            await _log_action("stale_page_marked", slug=row["slug"])
            refreshed += 1
        except Exception as exc:  # noqa: BLE001
            logger.warning("stale page refresh failed for %s: %s", row.get("slug"), exc)

    return {"ok": True, "refreshed": refreshed, "considered": len(rows)}


